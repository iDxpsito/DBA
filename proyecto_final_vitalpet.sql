-- ================================================================
-- COMPONENTE B: ACID Y CONTROL DE CONCURRENCIA        [25% de la nota]
-- NOTA: en una sola sesion (un solo F5) los bloques no chocan, por lo que
-- ambos obtienen el lock y hacen COMMIT. Para demostrar el bloqueo real,
-- abre dos ventanas SQL en Live SQL y ejecuta el primer SELECT ... FOR UPDATE
-- en una sin hacer COMMIT, y luego corre estos bloques en la otra.
-- ================================================================

-- ----------------------------------------------------------------
-- ESCENARIO 1: FOR UPDATE NOWAIT  (falla en 0 ms si la fila esta bloqueada)
-- ----------------------------------------------------------------
DECLARE
  v_nombre   medicamentos.nombre%TYPE;
  v_stock    medicamentos.stock%TYPE;
  v_cantidad NUMBER := 1;
  v_med_id   NUMBER := 1;
  recurso_ocupado EXCEPTION;
  PRAGMA EXCEPTION_INIT(recurso_ocupado, -54);  -- ORA-00054: resource busy (NOWAIT)
BEGIN
  DBMS_OUTPUT.PUT_LINE('=== ESCENARIO NOWAIT ===');
  SELECT nombre, stock INTO v_nombre, v_stock
  FROM medicamentos
  WHERE medicamento_id = v_med_id
  FOR UPDATE NOWAIT;                              -- lee y bloquea de forma atomica
  DBMS_OUTPUT.PUT_LINE('Lock obtenido: ' || v_nombre || ' | Stock: ' || v_stock);
  IF v_stock >= v_cantidad THEN
    UPDATE medicamentos SET stock = stock - v_cantidad WHERE medicamento_id = v_med_id;
    COMMIT;                                        -- persiste y libera el lock (Durabilidad)
    DBMS_OUTPUT.PUT_LINE('EXITO: descontada ' || v_cantidad || ' unidad. Stock: ' || (v_stock - v_cantidad));
  ELSE
    ROLLBACK;
    DBMS_OUTPUT.PUT_LINE('RECHAZADO: stock insuficiente.');
  END IF;
EXCEPTION
  WHEN recurso_ocupado THEN
    ROLLBACK;                                      -- Atomicidad
    DBMS_OUTPUT.PUT_LINE('ALERTA: medicamento en uso por otra transaccion (ORA-00054). Reintenta.');
  WHEN OTHERS THEN
    ROLLBACK;
    DBMS_OUTPUT.PUT_LINE('ERROR CRITICO: ' || SQLERRM);
END;
/

-- ----------------------------------------------------------------
-- ESCENARIO 2: FOR UPDATE WAIT 3  (espera 3 s; si no se libera, ORA-30006)
-- ----------------------------------------------------------------
DECLARE
  v_stock  medicamentos.stock%TYPE;
  v_med_id NUMBER := 1;
  timeout_expirado EXCEPTION;
  PRAGMA EXCEPTION_INIT(timeout_expirado, -30006); -- ORA-30006: WAIT timeout expired
BEGIN
  DBMS_OUTPUT.PUT_LINE('=== ESCENARIO WAIT 3 ===');
  SELECT stock INTO v_stock
  FROM medicamentos
  WHERE medicamento_id = v_med_id
  FOR UPDATE WAIT 3;                               -- espera hasta 3 s por el lock
  DBMS_OUTPUT.PUT_LINE('Lock obtenido en menos de 3 s. Stock actual: ' || v_stock);
  COMMIT;
  DBMS_OUTPUT.PUT_LINE('Operacion completada. Lock liberado.');
EXCEPTION
  WHEN timeout_expirado THEN
    ROLLBACK;
    DBMS_OUTPUT.PUT_LINE('TIMEOUT: el recurso no se libero en 3 segundos (ORA-30006).');
  WHEN OTHERS THEN
    ROLLBACK;
    DBMS_OUTPUT.PUT_LINE('ERROR: ' || SQLERRM);
END;
/

