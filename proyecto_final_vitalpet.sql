-- ----------------------------------------------------------------
-- OBJETO: PROCEDURE registrar_atencion
-- PROPOSITO: crear una consulta, recetar un medicamento, descontar stock y calcular total
-- PARAMETROS: p_mascota_id, p_vet_id, p_medicamento_id, p_cantidad (todos IN)
-- TABLAS AFECTADAS: consultas (INSERT/UPDATE), medicamentos_consulta (INSERT), medicamentos (UPDATE)
-- EXCEPCIONES: -20010 sin stock / NO_DATA_FOUND medicamento no encontrado
-- GARANTIA: si cualquier paso falla, ROLLBACK revierte todo (Atomicidad)
-- ----------------------------------------------------------------
CREATE OR REPLACE PROCEDURE registrar_atencion(
  p_mascota_id     IN NUMBER,
  p_vet_id         IN NUMBER,
  p_medicamento_id IN NUMBER,
  p_cantidad       IN NUMBER
)
IS
  v_nombre   medicamentos.nombre%TYPE;
  v_precio   medicamentos.precio_venta%TYPE;
  v_stock    medicamentos.stock%TYPE;
  v_consulta consultas.consulta_id%TYPE;
BEGIN
  -- Paso 1: obtener datos del medicamento
  SELECT nombre, precio_venta, stock
  INTO v_nombre, v_precio, v_stock
  FROM medicamentos
  WHERE medicamento_id = p_medicamento_id;

  -- Paso 2: validacion de negocio (el trigger tambien la refuerza)
  IF v_stock < p_cantidad THEN  -- no se receta mas de lo que hay (regla de negocio)
    RAISE_APPLICATION_ERROR(-20010,
      'No hay stock de ' || v_nombre || ' (disponible: ' || v_stock || ').');
  END IF;

  -- Paso 3: crear la cabecera de la consulta
  INSERT INTO consultas (mascota_id, vet_id, diagnostico, estado, datos_consulta)
  VALUES (p_mascota_id, p_vet_id, 'Atencion con formula medica', 'atendida',
          t_consulta(38.5, NULL, 'Generada por el procedimiento'))
  RETURNING consulta_id INTO v_consulta;  -- captura el ID autogenerado

  -- Paso 4: registrar el medicamento recetado (dispara el trigger de validacion)
  INSERT INTO medicamentos_consulta (consulta_id, medicamento_id, cantidad, costo_unit)
  VALUES (v_consulta, p_medicamento_id, p_cantidad, v_precio);

  -- Paso 5: descontar el stock (dispara el trigger de auditoria)
  UPDATE medicamentos SET stock = stock - p_cantidad
  WHERE medicamento_id = p_medicamento_id;

  -- Paso 6: calcular el total de la consulta
  UPDATE consultas SET total = p_cantidad * v_precio
  WHERE consulta_id = v_consulta;

  -- Paso 7: confirmar todos los cambios (Durabilidad)
  COMMIT;
  DBMS_OUTPUT.PUT_LINE('Consulta #' || v_consulta || ' registrada: ' ||
    p_cantidad || ' x ' || v_nombre || ' (Total: ' || (p_cantidad * v_precio) || ')');
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    ROLLBACK;
    DBMS_OUTPUT.PUT_LINE('ERROR: medicamento no encontrado.');
  WHEN OTHERS THEN
    ROLLBACK;  -- Atomicidad: revierte cualquier cambio parcial
    DBMS_OUTPUT.PUT_LINE('ERROR: ' || SQLERRM);
END;
/

-- PRUEBA del Procedure: caso exitoso
BEGIN
  registrar_atencion(p_mascota_id => 1, p_vet_id => 1, p_medicamento_id => 1, p_cantidad => 3);
END;
