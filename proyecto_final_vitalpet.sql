-- ----------------------------------------------------------------
-- OBJETO: TRIGGER trg_validar_stock_medicamento
-- PROPOSITO: impedir recetar mas medicamento del que hay en inventario
-- TABLAS AFECTADAS: lee medicamentos / valida insert en medicamentos_consulta
-- EXCEPCIONES: -20001 stock insuficiente / -20002 medicamento inexistente
-- ----------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_validar_stock_medicamento
  BEFORE INSERT ON medicamentos_consulta
  FOR EACH ROW
DECLARE
  v_stock  medicamentos.stock%TYPE;
  v_nombre medicamentos.nombre%TYPE;
BEGIN
  -- :NEW.medicamento_id = valor que se intenta insertar
  SELECT stock, nombre INTO v_stock, v_nombre
  FROM medicamentos
  WHERE medicamento_id = :NEW.medicamento_id;

  IF v_stock < :NEW.cantidad THEN
    RAISE_APPLICATION_ERROR(-20001,
      'Stock insuficiente de ' || v_nombre ||
      '. Disponible: ' || v_stock || ' / Solicitado: ' || :NEW.cantidad);
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(-20002, 'El medicamento ID ' || :NEW.medicamento_id || ' no existe.');
END;
/

-- ----------------------------------------------------------------
-- OBJETO: TRIGGER trg_auditar_stock  (BONUS: auditoria)
-- PROPOSITO: registrar en log_auditoria cada cambio de stock
-- ----------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_auditar_stock
  AFTER UPDATE OF stock ON medicamentos
  FOR EACH ROW
BEGIN
  INSERT INTO log_auditoria (tabla_origen, campo, valor_antes, valor_despues, operacion)
  VALUES ('MEDICAMENTOS', 'stock', TO_CHAR(:OLD.stock), TO_CHAR(:NEW.stock), 'UPDATE');
END;
