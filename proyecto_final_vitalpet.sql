SELECT * FROM v_ingresos_por_veterinario;

-- Filtrar sobre la vista de reporte (demuestra que es consultable como una tabla)
SELECT veterinario, ingreso_generado
FROM v_ingresos_por_veterinario
WHERE total_consultas >= 1;

