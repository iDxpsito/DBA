-- ----------------------------------------------------------------
-- OBJETO: VISTA v_catalogo_medicamentos  (vista de SEGURIDAD)
-- PROPOSITO: exponer el catalogo al cliente/recepcion sin datos sensibles
-- OCULTA: costo_interno (margen) y stock_minimo (dato logistico interno)
-- ----------------------------------------------------------------
CREATE OR REPLACE VIEW v_catalogo_medicamentos AS
SELECT
  medicamento_id,
  nombre AS medicamento,
  categoria,
  precio_venta,
  CASE
    WHEN stock = 0 THEN 'Agotado'
    WHEN stock < 5 THEN 'Pocas unidades'
    ELSE 'Disponible'
  END AS disponibilidad
  -- costo_interno y stock_minimo NO se incluyen (comercialmente sensibles)
FROM medicamentos;

-- ----------------------------------------------------------------
-- OBJETO: VISTA v_ingresos_por_veterinario  (vista de REPORTE)
-- PROPOSITO: responder "que veterinario genera mas ingresos por formulas y cuantas consultas atiende"
-- TECNICA: JOIN de 3 tablas + GROUP BY + SUM + COUNT + AVG + MAX
-- ----------------------------------------------------------------
CREATE OR REPLACE VIEW v_ingresos_por_veterinario AS
SELECT
  vt.nombre        AS veterinario,
  vt.especialidad,
  COUNT(DISTINCT c.consulta_id)        AS total_consultas,
  SUM(mc.cantidad)                     AS unidades_medicamento,
  SUM(mc.cantidad * mc.costo_unit)     AS ingreso_generado,
  ROUND(AVG(mc.cantidad), 1)           AS promedio_unidades,
  MAX(c.fecha)                         AS ultima_atencion
FROM veterinarios vt
JOIN consultas c              ON vt.vet_id = c.vet_id
JOIN medicamentos_consulta mc ON c.consulta_id = mc.consulta_id
WHERE c.estado <> 'cancelada'   -- excluir consultas canceladas del reporte
GROUP BY vt.nombre, vt.especialidad
ORDER BY ingreso_generado DESC;

