-- PASO 2: Clientes (titulares de la cuenta en la optica)
CREATE TABLE clientes (
    cliente_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre     VARCHAR2(100) NOT NULL,
    telefono   VARCHAR2(15),
    email      VARCHAR2(100)
);

-- PASO 3: Optometras (profesionales que realizan el examen visual)
CREATE TABLE optometras (
    optometra_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre       VARCHAR2(100) NOT NULL,
    especialidad VARCHAR2(60),
    tarifa_min   NUMBER(8,2) NOT NULL,   -- precio por minuto de consulta
    CHECK (tarifa_min > 0)
);

-- PASO 4: Pacientes (personas examinadas; un cliente puede traer a su familia)
CREATE TABLE pacientes (
    paciente_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cliente_id  NUMBER NOT NULL,
    nombre      VARCHAR2(60) NOT NULL,
    parentesco  VARCHAR2(40) NOT NULL,   -- Titular, Hijo/a, Conyuge...
    edad        NUMBER,
    CONSTRAINT fk_paciente_cliente FOREIGN KEY (cliente_id)
        REFERENCES clientes(cliente_id),
    CONSTRAINT chk_edad CHECK (edad > 0)
);

-- PASO 5: Examenes (cada examen lo realiza un optometra a un paciente)
-- La columna objeto datos_examen se agrega en el Ejercicio 3 con ALTER TABLE.
CREATE TABLE examenes (
    examen_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    paciente_id  NUMBER NOT NULL,
    optometra_id NUMBER NOT NULL,
    fecha        DATE DEFAULT SYSDATE,
    diagnostico  VARCHAR2(300),
    CONSTRAINT fk_examen_paciente FOREIGN KEY (paciente_id)
        REFERENCES pacientes(paciente_id),
    CONSTRAINT fk_examen_optometra FOREIGN KEY (optometra_id)
        REFERENCES optometras(optometra_id)
);

