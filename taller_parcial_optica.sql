-- EJERCICIO 6 (10 pts) - Vista segura: historial para el cliente
-- El cliente ve el historial de sus pacientes, SIN tarifas ni costos
-- internos de la optica (informacion financiera).
-- ============================================================
CREATE OR REPLACE VIEW v_historial_examen AS
SELECT p.nombre       AS paciente,
       p.parentesco,
       c.nombre       AS cliente,
       o.nombre       AS optometra,
       o.especialidad,
       e.diagnostico,
       e.fecha
FROM   examenes  e
JOIN   pacientes  p ON e.paciente_id  = p.paciente_id
JOIN   clientes   c ON p.cliente_id   = c.cliente_id
JOIN   optometras o ON e.optometra_id = o.optometra_id;

