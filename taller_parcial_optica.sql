-- EJERCICIO 3 (25 pts) - CREATE TYPE
-- Tipo t_examen que encapsula la atencion y calcula el honorario
-- DENTRO de la base de datos (no en la aplicacion).
-- ============================================================

-- Especificacion del tipo
CREATE OR REPLACE TYPE t_examen AS OBJECT (
    optometra    VARCHAR2(100),
    duracion_min NUMBER,
    tarifa_min   NUMBER(8,2),
    es_urgencia  NUMBER,            -- 1 = urgencia, 0 = normal
    MEMBER FUNCTION calcular_honorario RETURN NUMBER
);
/

-- Cuerpo del tipo
CREATE OR REPLACE TYPE BODY t_examen AS
    MEMBER FUNCTION calcular_honorario RETURN NUMBER IS
        v_base NUMBER;
    BEGIN
        v_base := SELF.duracion_min * SELF.tarifa_min;
        IF SELF.es_urgencia = 1 THEN
            RETURN v_base * 1.50;     -- recargo de urgencia del 50%
        ELSE
            RETURN v_base;
        END IF;
    END;
END;
/

-- Agregar la columna objeto a la tabla examenes
ALTER TABLE examenes ADD datos_examen t_examen;

-- Insertar un examen completo usando el constructor del tipo
-- (urgencia de 30 min con la Dra. Laura, tarifa 8500/min)
INSERT INTO examenes (paciente_id, optometra_id, diagnostico, datos_examen)
VALUES (1, 1, 'Urgencia: cuerpo extrano en el ojo',
        t_examen('Dra. Laura Ospina', 30, 8500, 1));
COMMIT;

-- Invocar el metodo (el alias de tabla 'e' es OBLIGATORIO)
SELECT e.examen_id,
       e.datos_examen.optometra            AS optometra,
       e.datos_examen.calcular_honorario() AS honorario
FROM   examenes e
WHERE  e.datos_examen IS NOT NULL;
-- Esperado: 30 * 8500 * 1.50 = 382500


-- ============================================================
-- EJERCICIO 4 (15 pts) - ACID y Concurrencia: FOR UPDATE NOWAIT
-- Bloquea la fila del optometra para que dos recepcionistas no
-- lo asignen al mismo tiempo. Captura ORA-00054 (recurso ocupado).
-- ============================================================
DECLARE
    v_nombre  VARCHAR2(100);
    v_tarifa  NUMBER(8,2);
    e_ocupado EXCEPTION;
    PRAGMA EXCEPTION_INIT(e_ocupado, -54);   -- ORA-00054
BEGIN
    SELECT nombre, tarifa_min
    INTO   v_nombre, v_tarifa
    FROM   optometras
    WHERE  optometra_id = 1
    FOR UPDATE NOWAIT;

    DBMS_OUTPUT.PUT_LINE('Optometra: ' || v_nombre || ' | Tarifa/min: ' || v_tarifa);
    COMMIT;
EXCEPTION
    WHEN e_ocupado THEN
        DBMS_OUTPUT.PUT_LINE('Optometra ocupado. Intenta en un momento.');
END;
/


-- ============================================================
-- EJERCICIO 5 (10 pts) - Trigger BEFORE INSERT
-- Impide que un mismo optometra tenga dos examenes el mismo dia.
-- ============================================================
CREATE OR REPLACE TRIGGER trg_optometra_unico_dia
    BEFORE INSERT ON examenes
    FOR EACH ROW
DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO   v_count
    FROM   examenes
    WHERE  optometra_id = :NEW.optometra_id
    AND    TRUNC(fecha) = TRUNC(:NEW.fecha);

    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20001,
            'El optometra ya tiene un examen registrado para esta fecha.');
    END IF;
END;
/

