CREATE TABLE inventario_celulares (
    celular_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    modelo       VARCHAR2(100) NOT NULL,
    stock        NUMBER        NOT NULL,
    precio       NUMBER(10,2)  NOT NULL,
    costo_interno NUMBER(10,2),
    CONSTRAINT chk_stock_no_negativo CHECK (stock >= 0)
);

CREATE TABLE detalles_mantenimiento (
    servicio_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    celular_id       NUMBER,
    tecnico          VARCHAR2(100) NOT NULL,
    repuestos_usados NUMBER DEFAULT 0,
    fecha_servicio   DATE DEFAULT SYSDATE,
    costo_repuestos  NUMBER(10,2),
    CONSTRAINT fk_celular_servicio FOREIGN KEY (celular_id) REFERENCES inventario_celulares(celular_id)
);

