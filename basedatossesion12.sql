CREATE OR REPLACE VIEW vista_cliente_catalogo AS
SELECT celular_id, modelo, precio AS precio_venta,
       CASE WHEN stock = 0 THEN 'Agotado' WHEN stock <= 2 THEN 'Ultimas unidades' ELSE 'En stock' END AS disponibilidad
FROM inventario_celulares WHERE stock >= 0;
/

CREATE OR REPLACE VIEW vista_tecnico_panel AS
SELECT ic.celular_id, ic.modelo, ic.stock AS repuestos_disponibles, dm.servicio_id, dm.tecnico, dm.repuestos_usados, dm.fecha_servicio, dm.costo_repuestos
FROM inventario_celulares ic LEFT JOIN detalles_mantenimiento dm ON ic.celular_id = dm.celular_id
ORDER BY dm.fecha_servicio DESC NULLS LAST;
/

CREATE OR REPLACE VIEW vista_gerencial_reporte AS
SELECT ic.modelo, ic.stock AS stock_actual, ic.precio AS precio_venta, ic.costo_interno,
       ROUND(((ic.precio - ic.costo_interno) / ic.precio) * 100, 1) AS margen_pct,
       COUNT(dm.servicio_id) AS total_servicios, NVL(SUM(dm.repuestos_usados), 0) AS repuestos_consumidos, NVL(SUM(dm.costo_repuestos), 0) AS gasto_repuestos,
       CASE WHEN ic.stock = 0 THEN 'CRITICO: Sin stock' WHEN ic.stock <= 2 THEN 'ALERTA: Stock bajo' ELSE 'Normal' END AS estado_inventory
FROM inventario_celulares ic LEFT JOIN detalles_mantenimiento dm ON ic.celular_id = dm.celular_id
GROUP BY ic.celular_id, ic.modelo, ic.stock, ic.precio, ic.costo_interno
ORDER BY ic.stock ASC;
/

